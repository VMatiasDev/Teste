const a = 7 // = está atribuindo um valor
let b = 3

b += a // atribuição aditiva b = b + a (está atribuindo e adicionando valor de a à variavel b)
console.log(b)

b -= 4 // b = b - 4
console.log(b)

b *= 2 // b = b * 2
console.log(b)

b /= 2 // b = b / 2
console.log(b)

b %= 2 // b = b % 2 // valor modular / Resto da divisão (se for par é 0, se for impar é 1)
console.log(b)